public class Act12_IncrementDecrementOperators {
    public static void main(String[] args) {
        boolean x;
        int y = 5;
        int a = 3;
        int b;

        x = true;
        System.out.println(x);

        y++;
        System.out.println(y * a);

        x = !x;
        System.out.println(x);

        a--;
        System.out.println(a);

        y--;
        b = y;
        System.out.println(b);

    }
}
